          <div class="content-txt">
            <p>The Automotive Component Manufacturers Association of India (ACMA) is the apex body representing the interest of the Indian Auto Component Industry. Its membership of over 850 manufacturers contributes to more than 85 per cent of the auto component industry’s turnover in the organized sector. ACMA is an ISO 9001:2015 Certified Association.</p>
            <p>ACMA’s charter is to develop a globally competitive Indian Auto Component Industry & strengthen its role in national economic development as also promote business through international alliances.</p>
            <p>The auto component industry which spans across different geographies of the country, is a large, integrated and complex network of suppliers. Today, it contributes 2.3% of the country's National GDP, 25% of the national Manufacturing GDP and employs over five million people. The industry is dominated by SMEs, is one of the key drivers of India’s economic growth and the ‘Make in India’ program.</p>
            <p>The auto component industry manufactures a wide variety of products including engine parts, drive transmission and steering parts, body & chassis, suspension & braking parts, equipment & electrical parts, besides others.</p>
            <p>The vehicle industry in India witnessed two successive years of de-growth of 14.6 per cent in FY19-20 and thereafter of 13.6 per cent in FY20-21. The economic slowdown of FY19-20 was compounded by the challenges of a pandemic led national lockdown in FY20-21 resulting in decline in vehicle production & consumption. In tandem the component industry turnover in the first quarter of FY20-21 was a complete washout, the industry regained significant ground second quarter onwards, posting a subdued performance in FY20-21 with de-growth of 3 per cent over the previous year, registering a turnover of Rs.3.4 lakh crore (USD 45.9 billion).</p>
            <p>The sales to OEMs, in the domestic market stood at Rs.2.79 lakh crore (USD 37.7 billion), declining by 3 per cent, when compared to the previous year. Sales to OEMs in the H1 2021 had declined by 31 per cent over the first-half of the previous year, however it witnessed a healthy recovery in H2 2021.</p>
            <p>Exports of auto components witnessed degrowth of 8 per cent to Rs.0.98 lakh crore (USD 13.3 billion) in 2020-21 from Rs 1.02 lakh crore (USD 14.5 billion) in 2019-20. Europe accounting for 32 per cent of exports, saw a decline of 4 percent, while North America & Asia, accounting for 30 per cent and 26 per cent declined 7 per cent and 8 per cent respectively.</p>
            <p>Meanwhile, slowdown in the domestic market also reflected on imports of component into India. Component imports fell by 11 per cent to Rs.1.02 lakh crore (USD 13.8 billion) in 2020-21 from Rs.1.09 lakh crore (USD 15.4 billion) in 2019-20. Asia accounted for 66 per cent of imports followed by Europe and North America at 25 per cent and 7 per cent respectively. Imports from Asia declined by 9 per cent, while those from Europe by 13 per cent and from North America by 17 per cent.</p>
            <p>The two-wheelers & passenger vehicles segments of the aftermarket witnessed strong recovery, revival in the commercial vehicles aftermarket was less than expected leading to an overall decline of 7 per cent in the aftermarket. The turnover of the aftermarket in FY 20-21 stood at Rs. 64,524 crore (USD 8.7 billion) compared to Rs. 69.381 crore (USD 9.8 billion) in the previous year.</p>
            <p>The expected outlook for the FY 22, provides a glimpse of hope, with steady economic recovery and enhanced focus on vaccination, consumer sentiment seems to be bouncing back. Whilst demand seems to be returning to the automotive sector, some of the key projection for the different category of vehicles are:  Passenger vehicles are expected to register a growth of 8 – 10 per cent, commercial vehicle is expected to bounce back strongly with 20 per cent growth, while the two wheelers may register 5 per cent degrowth and farm equipment & tractors may remain flat in the FY 22. In this backdrop, the component industry is expected to grow in the range of 10 - 15 per cent respectively.</p>
